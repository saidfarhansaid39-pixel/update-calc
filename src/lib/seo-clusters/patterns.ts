export interface ClusterTemplate {
  suffix: string
  titleTemplate: string
  descriptionTemplate: string
  h1Template: string
  introTemplate: string
  searchIntent: string
  audience: string
  faqTemplates: Array<{ q: string; a: string }>
  applicableSlugPredicate?: (slug: string, title: string) => boolean
}

export function fill(template: string, vars: Record<string, string>): string {
  let result = template
  for (const [key, val] of Object.entries(vars)) {
    result = result.replaceAll(`{${key}}`, val)
  }
  return result
}

export function calcVars(slug: string, title: string, description: string): Record<string, string> {
  const shortTitle = title.replace(/ Calculator$/i, '').replace(/ Converter$/i, '').replace(/ Tool$/i, '')
  const toolType = title.includes('Calculator') ? 'calculator'
    : title.includes('Converter') ? 'converter'
    : title.includes('Tool') ? 'tool' : 'calculator'
  return { slug, title, shortTitle, toolType, description }
}

