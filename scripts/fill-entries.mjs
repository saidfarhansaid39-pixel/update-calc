// Helper to generate entries from hub data
function generateEntries(hubData) {
  const results = [];
  const hub = hubData.hub;
  const hubSlug = hubData.slugs[0];
  const hubName = hubData.names[0];
  const additions = hubData.additions;
  
  for (let i = 0; i < additions.length; i++) {
    const entry = additions[i];
    let slug = entry[0];
    const title = entry[1];
    const kw = entry[2];
    const additionalKW = entry.slice(3).flat();
    const allKW = [...kw, ...additionalKW].slice(0, 6);
    
    // Make slug unique if it exists
    let finalSlug = slug;
    let suffix = 1;
    while (existingSlugs.has(finalSlug)) {
      suffix++;
      finalSlug = `${slug}-${suffix}`;
    }
    
    const desc = `Free ${title} — ${DESCS[hub]}`;
    const tier = tierFor(i, additions.length);
    
    results.push(`  { slug: '${finalSlug}', title: '${title}', description: '${desc}', category: '${hub}', tier: '${tier}', hubSlug: '${hubSlug}', hubName: '${hubName}', keywords: ${JSON.stringify(allKW)} }`);
    existingSlugs.add(finalSlug);
  }
  
  return results;
}

